<script>
window.addEventListener('scroll', function() {
    const form = document.getElementById('stickyForm');
    const offsetTop = form.offsetTop;
    const scrollY = window.scrollY;
    // Only apply sticky if screen is wider than 768px
    if (window.innerWidth > 768) {
        if (scrollY > offsetTop - 100) {
            form.classList.add('fixed-form');
        } else {
            form.classList.remove('fixed-form');
        }
    } else {
        // Ensure it's removed on mobile
        form.classList.remove('fixed-form');
    }
});
// Also handle window resize
window.addEventListener('resize', () => {
    const form = document.getElementById('stickyForm');
    if (window.innerWidth <= 768) {
        form.classList.remove('fixed-form');
    }
});
</script>

