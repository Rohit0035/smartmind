<script>
let chartInstance;

function calculateEMI() {
    const principal = parseFloat(document.getElementById('loanAmount').value);
    const tenure = parseFloat(document.getElementById('loanTenure').value) * 12;
    const annualRate = parseFloat(document.getElementById('interestRate').value);
    const monthlyRate = annualRate / (12 * 100);

    const emi = (principal * monthlyRate * Math.pow(1 + monthlyRate, tenure)) /
        (Math.pow(1 + monthlyRate, tenure) - 1);

    const totalPayment = emi * tenure;
    const totalInterest = totalPayment - principal;

    document.getElementById('emiAmount').innerText = "₹" + emi.toFixed(0);
    document.getElementById('principalDisplay').innerText = principal.toFixed(0);
    document.getElementById('interestDisplay').innerText = totalInterest.toFixed(0);

    const ctx = document.getElementById('emiChart').getContext('2d');
    if (chartInstance) {
        chartInstance.destroy();
    }

    chartInstance = new Chart(ctx, {
        type: 'pie',
        data: {
            labels: ['Principal', 'Interest'],
            datasets: [{
                data: [principal, totalInterest],
                backgroundColor: ['#198754', '#f0ad4e'],
            }]
        },
        options: {
            responsive: true,
            plugins: {
                legend: {
                    position: 'bottom'
                },
                tooltip: {
                    callbacks: {
                        label: function(context) {
                            return context.label + ': ₹' + context.raw.toFixed(0);
                        }
                    }
                }
            }
        }
    });
}
</script>

